static const struct {
	size_t nr_ocall;
	void * table[12];
} ocall_table_Enclave = {
	12,
	{
		(void*)Enclave_ocall_print_string,
		(void*)Enclave_ocall_log,
		(void*)Enclave_u_sgxssl_ftime,
		(void*)Enclave_sgx_oc_cpuidex,
		(void*)Enclave_sgx_thread_wait_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_set_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_setwait_untrusted_events_ocall,
		(void*)Enclave_sgx_thread_set_multiple_untrusted_events_ocall,
		(void*)Enclave_create_session_ocall,
		(void*)Enclave_exchange_report_ocall,
		(void*)Enclave_close_session_ocall,
		(void*)Enclave_invoke_service_ocall,
	}
};
sgx_status_t ecall_say_hello(sgx_enclave_id_t eid, const char* name, size_t len)
{
	sgx_status_t status;
	ms_ecall_say_hello_t ms;
	ms.ms_name = (char*)name;
	ms.ms_len = len;
	status = sgx_ecall(eid, 0, &ocall_table_Enclave, &ms);
	return status;
}

